    private void privilegiosUsuario(){
        ResultSet resultado1=resultadoConsulta("SELECT* FROM Usuario where id="+usuario+";");
        try {
            resultado1.next();
            int tipoUsuario=resultado1.getInt("Tipo_Usuario_id");
            ResultSet resultado2=resultadoConsulta("select P.Modulo from Permiso_Usuario PU inner join Permiso P on PU.Permiso_id=P.id where PU.Tipo_Usuario_id="+tipoUsuario+";");
            while(resultado2.next()==true){
                modulosPermitidos.add(resultado2.getString("Modulo"));
                System.out.println(resultado2.getString("Modulo"));
                habilitarModulos();
            }
        } catch (SQLException ex) {
            Logger.getLogger(PrincipalUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
