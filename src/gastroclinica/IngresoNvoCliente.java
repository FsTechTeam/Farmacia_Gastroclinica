            PreparedStatement pst = conexion.prepareStatement("insert into Cliente(Nombre, Apellido, Ciudad, NIT) values(?,?,?,?)");
                                   
                pst.setString(1, nombre);
                pst.setString(2, apellido);
                pst.setString(3, direccion);
                pst.setString(4, nit);
