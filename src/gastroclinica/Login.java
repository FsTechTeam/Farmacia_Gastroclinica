    public void acceder() {
        try {
            //chekar si el usuario escrbio el nombre de usuario y pw
            if (jTextField1.getText().length() > 0 && jPasswordField1.getText().length() > 0) {
                // Si el usuario si fue validado correctamente
                if (validarUsuario(jTextField1.getText(), jPasswordField1.getText())) {
                    System.out.println(resultadosConsulta.getInt("Tipo_Usuario_id"));
                    PrincipalUsuario acceso=new PrincipalUsuario(resultadosConsulta.getInt("id"),conexion);
                    setVisible(false);
                    acceso.setVisible(true);
                    
                } else {
                    JOptionPane.showMessageDialog(null, "El nombre de usuario y/o contrase単a no son validos.");
                    jTextField1.setText("");    //limpiar campos
                    jPasswordField1.setText("");
                    jTextField1.requestFocusInWindow();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe escribir nombre de usuario y contrase単a.\n"
                        + "NO puede dejar ningun campo vacio");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    boolean validarUsuario(String elUsr, String elPw) throws IOException {
        try {
            Statement instruccionSQL = conexion.createStatement();
            resultadosConsulta = instruccionSQL.executeQuery("SELECT * FROM Usuario WHERE Usuario='" + elUsr + "' AND Contrase単a=('" + elPw + "');");

            if (resultadosConsulta.next() == true) {
                return true;
            } 
            else {
                return false;
            }
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }
    }
    
        

