    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText()!=null&&jTextField1.getText()!=null
                &&jTextField1.getText().compareTo("")!=0&&jTextField1.getText().compareTo("")!=0){
            nombre=jTextField1.getText();
            fabricante=jTextField2.getText();
            Object[] seleccion = jList1.getSelectedValues();
            ResultSet verificar=devolverResultado("Select* from Producto where Nombre=\""+nombre+"\" and Fabricante=\""+fabricante+"\";");
            try {
                if(verificar.next()==false){
                    consultaInsertar("insert into Producto(Nombre,Fabricante) values(\""+nombre+"\",\""+fabricante+"\");");
                    JOptionPane.showMessageDialog(null,"Producto agregado a la base de datos.","",JOptionPane.INFORMATION_MESSAGE);
                    ResultSet obtenerIDNP=devolverResultado("Select* from Producto where Nombre=\""+nombre+"\" and Fabricante=\""+fabricante+"\";");
                    obtenerIDNP.next();
                    int idProducto=obtenerIDNP.getInt("id");
                    for(int i=0;i<seleccion.length;i++){
                        ResultSet categoria=devolverResultado("select* from Categoria where Categoria=\""+seleccion[i].toString()+"\";");
                        categoria.next();
                        int idCategoria=categoria.getInt("id");
                        ResultSet comprobar=devolverResultado("select* from Producto_Categoria where Categoria_id=\""+idCategoria+"\" and Producto_id=\""+idProducto+"\";");
                        if(comprobar.next()!=true){
                            consultaInsertar("insert into Producto_Categoria(Categoria_id,Producto_id)"
                                    + "values(\""+idCategoria+"\",\""+idProducto+"\");");
                        }
                        AgregarProducto.actualizarProd=true;
                        this.dispose();
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null,"El producto con el fabricante ingresado ya existe en la base de datos..","Imposible almacenar",JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Producto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"No deje ningun campo vacio.","",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

